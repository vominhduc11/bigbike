#!/usr/bin/env node
// Parse `kd_rank_math_redirections` INSERT line from WP dump and emit
// SQL INSERT statements for the Postgres `redirects` table.
//
// Usage: node extract-wp-redirects.mjs <input.sql> <output.sql>
//
// The serialized PHP `sources` blob holds N pattern entries. We emit one
// `redirects` row per pattern, deduplicated by source_pattern.

import fs from "node:fs";

const [, , inputPath, outputPath] = process.argv;
if (!inputPath || !outputPath) {
  console.error("usage: node extract-wp-redirects.mjs <input.sql> <output.sql>");
  process.exit(1);
}

const raw = fs.readFileSync(inputPath, "utf8");

// Strip prefix "INSERT INTO `kd_rank_math_redirections` VALUES "
const valuesStart = raw.indexOf("VALUES ");
if (valuesStart < 0) {
  console.error("no VALUES clause found");
  process.exit(1);
}
let body = raw.slice(valuesStart + "VALUES ".length).trim();
if (body.endsWith(";")) body = body.slice(0, -1);

// Tokenize into rows. Each row = (a, b, c, ...). Handle quoted strings with
// backslash escapes.
const rows = [];
let i = 0;
const N = body.length;

function parseRow() {
  // expect '('
  if (body[i] !== "(") throw new Error(`expected ( at ${i}, got ${body[i]}`);
  i++;
  const fields = [];
  let buf = "";
  let inStr = false;
  while (i < N) {
    const c = body[i];
    if (inStr) {
      if (c === "\\") {
        // backslash escape, take the next char literally
        buf += body[i + 1] ?? "";
        i += 2;
        continue;
      }
      if (c === "'") {
        inStr = false;
        i++;
        continue;
      }
      buf += c;
      i++;
      continue;
    }
    if (c === "'") {
      inStr = true;
      i++;
      continue;
    }
    if (c === ",") {
      fields.push(buf);
      buf = "";
      i++;
      continue;
    }
    if (c === ")") {
      fields.push(buf);
      i++;
      return fields;
    }
    buf += c;
    i++;
  }
  throw new Error("unexpected EOF");
}

while (i < N) {
  const row = parseRow();
  rows.push(row);
  // skip ',' between rows
  while (i < N && (body[i] === "," || body[i] === " " || body[i] === "\n")) i++;
}

console.error(`parsed ${rows.length} rank_math rows`);

// Each row: (id, sources, url_to, header_code, hits, status, created, updated, last_accessed)
// sources is serialized PHP. Extract patterns via regex: after `"pattern";s:N:"<text>"`.
const PATTERN_RE = /"pattern";s:\d+:"((?:[^"\\]|\\.)*)"/g;

function extractPatterns(serialized) {
  const out = [];
  let m;
  PATTERN_RE.lastIndex = 0;
  while ((m = PATTERN_RE.exec(serialized)) !== null) {
    // Unescape backslash quotes
    const p = m[1].replace(/\\"/g, '"').replace(/\\\\/g, "\\");
    if (p) out.push(p);
  }
  return out;
}

// Strip protocol+host, return path. If not a bigbike URL, keep full.
function normalizeTarget(url) {
  if (!url) return "/";
  let u = url.trim();
  // strip bigbike domains
  u = u.replace(/^https?:\/\/(www\.)?(beta\.)?bigbike\.vn/, "");
  if (!u.startsWith("/") && !u.startsWith("http")) {
    u = "/" + u;
  }
  return u || "/";
}

// Normalize source pattern: ensure leading slash, strip leading host if any.
function normalizeSource(pattern) {
  let p = pattern.trim();
  // Strip leading domain if present
  p = p.replace(/^https?:\/\/(www\.)?(beta\.)?bigbike\.vn/, "");
  p = p.replace(/^\/+/, "");
  return "/" + p;
}

const seen = new Set();
const out = [];
let totalPatterns = 0;
let inactiveSkipped = 0;

for (const row of rows) {
  const [legacyId, sources, urlTo, headerCode, , status] = row;
  if (status !== "active") {
    inactiveSkipped++;
    continue;
  }
  const target = normalizeTarget(urlTo);
  const code = parseInt(headerCode, 10) || 301;
  if (![301, 302, 307, 308].includes(code)) continue;

  const patterns = extractPatterns(sources);
  for (const p of patterns) {
    totalPatterns++;
    const src = normalizeSource(p);
    if (!src || src === "/") continue;
    if (src === target) continue;
    if (seen.has(src)) continue;
    seen.add(src);
    out.push({ src, target, code, legacyId });
  }
}

console.error(`extracted ${totalPatterns} patterns from ${rows.length} rows`);
console.error(`skipped ${inactiveSkipped} inactive rows`);
console.error(`unique source paths: ${out.length}`);

// Emit SQL Flyway migration
function sqlEscape(s) {
  return s.replace(/'/g, "''");
}

const lines = [];
lines.push("-- V106 — import legacy WP redirects from kd_rank_math_redirections");
lines.push("-- Generated by scripts/extract-wp-redirects.mjs");
lines.push("-- Source: bigbike_vn__2026_04_17/sqldump.sql");
lines.push("-- Patterns deduplicated by source_pattern (uq_redirects_source_pattern).");
lines.push("");
lines.push("BEGIN;");
lines.push("");
lines.push("INSERT INTO redirects (id, source_pattern, target_url, redirect_type, status_code, enabled, hit_count, legacy_id, created_at, updated_at)");
lines.push("VALUES");

const valueLines = out.map((r, idx) => {
  const last = idx === out.length - 1;
  // redirect_type = 'PERMANENT' to match backend RedirectImporter.java convention.
  return `  (gen_random_uuid(), '${sqlEscape(r.src)}', '${sqlEscape(r.target)}', 'PERMANENT', ${r.code}, true, 0, ${r.legacyId}, NOW(), NOW())${last ? "" : ","}`;
});

lines.push(...valueLines);
lines.push("ON CONFLICT (source_pattern) DO NOTHING;");
lines.push("");
lines.push("COMMIT;");
lines.push("");

fs.writeFileSync(outputPath, lines.join("\n"), "utf8");
console.error(`wrote ${out.length} INSERT rows to ${outputPath}`);

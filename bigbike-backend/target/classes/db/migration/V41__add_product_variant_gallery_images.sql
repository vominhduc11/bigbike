-- Per-variant image gallery (Phương án A — mirrors WP `rtwpvg_images` meta).
-- Each row is one image inside a single variant's gallery; `sort_order`
-- preserves the admin-defined sequence. Schema mirrors product_gallery_images
-- so the frontend ImageAsset shape applies unchanged.
create table product_variant_gallery_images (
    id bigint generated by default as identity primary key,
    variant_id varchar(64) not null,
    sort_order integer not null,
    image_id varchar(64),
    image_url text not null,
    image_alt varchar(255),
    image_width integer,
    image_height integer,
    image_mime_type varchar(100),
    constraint fk_product_variant_gallery_images_variant_id
        foreign key (variant_id) references product_variants (id) on delete cascade
);

create index idx_product_variant_gallery_images_variant_id
    on product_variant_gallery_images (variant_id, sort_order);

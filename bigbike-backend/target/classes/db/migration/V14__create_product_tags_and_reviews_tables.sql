-- V14: Product tags and product reviews
-- product_tags: denormalized join table (consistent with article_tags pattern)
-- reviews: WooCommerce product review comments with rating

create table if not exists product_tags (
    id          bigint generated by default as identity primary key,
    product_id  varchar(64) not null,
    tag         varchar(120) not null,
    constraint fk_product_tags_product_id
        foreign key (product_id) references products (id) on delete cascade
);

create index if not exists idx_product_tags_product_id on product_tags (product_id);
create index if not exists idx_product_tags_tag        on product_tags (tag);
create unique index if not exists idx_product_tags_product_tag on product_tags (product_id, tag);

create table if not exists reviews (
    id              bigint generated by default as identity primary key,
    product_id      varchar(64) not null,
    customer_id     uuid,
    author_name     varchar(255),
    author_email    varchar(255),
    rating          smallint not null,
    body            text,
    status          varchar(30) not null default 'APPROVED',
    legacy_id       bigint unique,
    created_at      timestamp with time zone not null,
    updated_at      timestamp with time zone not null,
    constraint ck_reviews_rating check (rating between 1 and 5),
    constraint fk_reviews_product_id
        foreign key (product_id) references products (id) on delete cascade,
    constraint fk_reviews_customer_id
        foreign key (customer_id) references customers (id) on delete set null
);

create index if not exists idx_reviews_product_id  on reviews (product_id);
create index if not exists idx_reviews_customer_id on reviews (customer_id);
create index if not exists idx_reviews_status      on reviews (status);
